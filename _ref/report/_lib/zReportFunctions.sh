_report_mismatch_procedure() {
	local currentFunction_fromExported
	currentFunction_fromExported="$1"
	local currentFunction_fromParagraph
	currentFunction_fromParagraph="$2"
	shift
	shift
	
	local current_written
	local current_exported
	current_written=$("$currentFunction_fromParagraph" "$1")
	current_exported=$("$currentFunction_fromExported" "$2" "$3")
	
	# DANGER: Items missing from exported will not be reported (usually this is neither desired nor an issue).
	[[ "$current_exported" == "" ]] && return 1
	
	if _safeEcho_newline "$current_written" | grep --fixed-strings "$current_exported" > /dev/null 2>&1 && _safeEcho_newline "$current_exported" | grep --fixed-strings "$current_written" > /dev/null 2>&1 
	then
		return 0
	fi
	
	_messageError 'mismatch'
	_messagePlain_bad 'written: '"$1"' '
	_safeEcho_newline "$current_written"
	_messagePlain_bad 'exported: '"$2"' '"$3"' '
	_safeEcho_newline "$current_exported"
	_safeEcho_newline
	
	return 1
}



_report_mismatch_fromParagraph_procedure() {
	local currentFile="$1"
	[[ ! -e "$currentFile" ]] && echo && return 0
	
	local currentMatchParameter1
	currentMatchParameter1='false'
	
	local currentLine
	local currentLinePrevious
	currentLinePrevious=""
	while read -r currentLine
	do
		if [[ "$currentLinePrevious" == "" ]]
		then
			_safeEcho_newline "$currentLine" | grep --line-regexp --fixed-strings "$2" > /dev/null 2>&1 && currentMatchParameter1='true'
		fi
		
		[[ "$currentMatchParameter1" == 'true' ]] && _safeEcho_newline "$currentLine" | _filter_badChars
		
		[[ "$currentMatchParameter1" == 'true' ]] && [[ "$currentLine" == "" ]] && return 0
		
		currentLinePrevious="$currentLine"
	done < "$currentFile"
	
	return 1
}










